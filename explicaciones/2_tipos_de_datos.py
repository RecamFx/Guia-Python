# Aclaraciones
# int() toma un texto o numero, y lo transforma en un entero (numero si coma)
# float() toma un texto o numero, y lo transforma un un flotante (numero con coma) 
# bool() toma 0 y 1, y lo transforma en un True o False
# str() toma un numero y lo transforma en un texto
# list() toma un array dentro del parentesis y lo divide por caracteres y lo transforma en una lista