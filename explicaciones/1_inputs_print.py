# PRINTS: Que es concatenar? Concatenar es la union de varios elementos (variables).
# En los prints se pueden usar el + o los fstrings

# Concatenando con +:
ejemplo = "concatenacion"
print("Hola " + "esto es una " + ejemplo + " con +") #? Hola esto es una concatenacion con +

# Concatenando con fstrings:
print(f"Hola esto es una {ejemplo} con fstrings") #? Hola esto es una concatenacion con fstrings