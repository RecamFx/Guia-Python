# INPUTS: Todo lo que se recibe de input es un string

# PRINTS: Que es concatenar? Concatenar es la union de varios elementos (variables).
# En los prints se pueden usar el + o los fstrings

# Concatenando con +:
ejemplo = "concatenacion"
print("Hola " + "esto es una " + ejemplo) #? Hola esto es una concatenacion

# Concatenando con fstrings:
print(f"Hola esto es una {ejemplo}") #? Hola esto es una concatenacion